def share_email(
    to_email: str,
    page_name: str,
    data: dict = None,
    subject: str = None,
) -> dict:
    """
    Queue an email via the database email_queue table.

    Returns ``{success: bool, message_id: str|None, error: str}``.
    """
    date_str = _date_display()
    subject = subject or f"PPS Anantam \u2014 {page_name} | {date_str}"
    body_html = format_message(page_name, data, channel="email")
    result = {"success": False, "message_id": None, "error": ""}

    try:
        from database import insert_email_queue, _now_ist as db_now  # noqa: F811

        queue_id = insert_email_queue({
            "to_email": to_email,
            "subject": subject,
            "body_html": body_html,
            "email_type": "share",
            "status": "queued",
            "scheduled_at": db_now(),
        })
        result["success"] = True
        result["message_id"] = f"email-{queue_id}"
        _log_share("email", page_name, to_email, "queued")

    except ImportError:
        # Database not available — return the HTML body so caller can still use it
        result["success"] = True
        result["message_id"] = f"email-local-{_gen_id()}"
        result["formatted_html"] = body_html
        result["subject"] = subject
        _log_share("email", page_name, to_email, "fallback",
                   "database module not available — HTML body returned")

    except Exception as exc:
        result["error"] = str(exc)
        _log_share("email", page_name, to_email, "error", str(exc))

    return result


