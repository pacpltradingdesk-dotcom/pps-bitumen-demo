# Scheduled Sharing
# ═══════════════════════════════════════════════════════════════════════════════

def schedule_share(
    page_name: str,
    data_fn: str,
    channel: str,
    schedule_time: str,
    recipients: list,
) -> dict:
    """
    Schedule a share for future execution.

    Saves to ``share_schedules.json``.  A separate scheduler process
    (or a Streamlit background thread) should poll this file and fire
    the shares at the appropriate time.

    Parameters
    ----------
    page_name : str
        Dashboard page to share.
    data_fn : str
        Fully-qualified function name that returns the data dict at
        execution time (e.g. ``"pages.dashboard.get_summary_data"``).
    channel : str
        ``"whatsapp"`` | ``"telegram"`` | ``"email"``.
    schedule_time : str
        ISO-8601 datetime string in IST (e.g. ``"2026-03-24 09:00:00"``).
    recipients : list[str]
        Phone numbers, chat IDs, or email addresses.

    Returns
    -------
    dict
        ``{success, schedule_id, error}``
    """
    schedule_id = _gen_id()
    entry = {
        "id": schedule_id,
        "page_name": page_name,
        "data_fn": data_fn,
        "channel": channel,
        "schedule_time": schedule_time,
        "recipients": recipients,
        "status": "scheduled",
        "created_at": _now_ist(),
    }

    try:
        schedules: list = []
        if _SCHEDULES_FILE.exists():
            raw = _SCHEDULES_FILE.read_text(encoding="utf-8").strip()
            if raw:
                schedules = json.loads(raw)

        schedules.append(entry)
        _SCHEDULES_FILE.write_text(
            json.dumps(schedules, indent=2, ensure_ascii=False),
            encoding="utf-8",
        )
        _log_share(channel, page_name, ", ".join(recipients), "scheduled")
        return {"success": True, "schedule_id": schedule_id, "error": ""}

    except Exception as exc:
        _log_share(channel, page_name, ", ".join(recipients), "schedule_error",
                   str(exc))
        return {"success": False, "schedule_id": None, "error": str(exc)}


