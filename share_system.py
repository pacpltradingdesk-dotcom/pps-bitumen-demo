def share_whatsapp(
    phone: str,
    page_name: str,
    data: dict = None,
) -> dict:
    """
    Send a message via the WhatsApp queue.

    Inserts into the *whatsapp_queue* table (database.py) with
    status='queued'.  If the database layer is unavailable, falls back to
    returning the formatted message text so the caller can still use it
    (e.g. copy-paste into WhatsApp Web).

    Returns ``{success: bool, message_id: str|None, error: str}``.
    """
    formatted = format_message(page_name, data, channel="whatsapp")
    result = {"success": False, "message_id": None, "error": ""}

    try:
        from database import insert_wa_queue, _now_ist as db_now  # noqa: F811

        queue_id = insert_wa_queue({
            "to_number": phone,
            "message_type": "share",
            "session_text": formatted,
            "status": "queued",
            "scheduled_at": db_now(),
        })
        result["success"] = True
        result["message_id"] = f"wa-{queue_id}"
        _log_share("whatsapp", page_name, phone, "queued")

    except ImportError:
        # Database module not available — return message as fallback
        result["success"] = True
        result["message_id"] = f"wa-local-{_gen_id()}"
        result["formatted_text"] = formatted
        _log_share("whatsapp", page_name, phone, "fallback",
                   "database module not available — message text returned")

    except Exception as exc:
        result["error"] = str(exc)
        _log_share("whatsapp", page_name, phone, "error", str(exc))

    return result


# ═══════════════════════════════════════════════════════════════════════════════
# Telegram
# ═══════════════════════════════════════════════════════════════════════════════

def share_telegram(
    chat_id: str,
    page_name: str,
    data: dict = None,
) -> dict:
    """
    Send a message via the Telegram engine.

    Imports ``send_message`` from ``telegram_engine`` and posts with
    ``parse_mode="HTML"``.

    Returns ``{success: bool, message_id: str|None, error: str}``.
    """
    formatted = format_message(page_name, data, channel="telegram")
    result = {"success": False, "message_id": None, "error": ""}

    try:
        from telegram_engine import send_message as tg_send  # type: ignore

        resp = tg_send(chat_id, formatted, parse_mode="HTML")
        # tg_send may return a dict with message_id or a raw response
        if isinstance(resp, dict):
            msg_id = resp.get("message_id") or resp.get("result", {}).get("message_id")
        else:
            msg_id = str(resp) if resp else None

        result["success"] = True
        result["message_id"] = f"tg-{msg_id}" if msg_id else f"tg-{_gen_id()}"
        _log_share("telegram", page_name, chat_id, "sent")

    except ImportError:
        # Telegram engine not available — return formatted text as fallback
        result["success"] = True
        result["message_id"] = f"tg-local-{_gen_id()}"
        result["formatted_text"] = formatted
        _log_share("telegram", page_name, chat_id, "fallback",
                   "telegram_engine module not available — message text returned")

    except Exception as exc:
        result["error"] = str(exc)
        _log_share("telegram", page_name, chat_id, "error", str(exc))

    return result


