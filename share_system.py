# PDF
# ═══════════════════════════════════════════════════════════════════════════════

def share_pdf(
    page_name: str,
    data: dict = None,
) -> bytes | None:
    """
    Generate PDF bytes for the given page data using PDFExportEngine.

    Returns raw PDF bytes, or *None* if PDF generation is unavailable.
    """
    try:
        from pdf_export_engine import PDFExportEngine  # type: ignore

        engine = PDFExportEngine(page_name)

        # Title
        engine.add_title(page_name)

        # Summary paragraph
        summary = (data or {}).get("summary", "")
        if summary:
            engine.add_section("Summary")
            engine.add_paragraph(summary)

        # KPIs
        kpis = (data or {}).get("kpis") or []
        if kpis:
            engine.add_section("Key Metrics")
            kpi_tuples = []
            for kpi in kpis:
                label = kpi.get("label", "")
                value = kpi.get("value", "")
                delta = kpi.get("delta", "")
                kpi_tuples.append((label, str(value), str(delta)))
            engine.add_kpi_row(kpi_tuples)

        # Table data
        table_data = (data or {}).get("table_data") or []
        if table_data:
            engine.add_section("Data")
            if isinstance(table_data[0], dict):
                headers = list(table_data[0].keys())
                rows = [[row.get(h, "") for h in headers] for row in table_data]
            elif isinstance(table_data[0], (list, tuple)):
                headers = [f"Col {i+1}" for i in range(len(table_data[0]))]
                rows = [list(r) for r in table_data]
            else:
                headers = ["Value"]
                rows = [[str(v)] for v in table_data]
            engine.add_table(headers, rows)

        pdf_bytes = engine.build(save_to_archive=False)
        _log_share("pdf", page_name, "local", "generated")
        return pdf_bytes

    except ImportError:
        _log_share("pdf", page_name, "local", "fallback",
                   "pdf_export_engine module not available")
        return None

    except Exception as exc:
        _log_share("pdf", page_name, "local", "error", str(exc))
        return None


