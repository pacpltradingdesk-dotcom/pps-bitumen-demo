# ═══════════════════════════════════════════════════════════════════════════════
# Helpers
# ═══════════════════════════════════════════════════════════════════════════════

def _now_ist() -> str:
    """Return current IST timestamp as ISO-8601 string."""
    return datetime.now(IST).strftime("%Y-%m-%d %H:%M:%S")


def _now_ist_dt() -> datetime:
    """Return current IST datetime object."""
    return datetime.now(IST)


def _date_display() -> str:
    """Human-readable date for messages, e.g. '23-Mar-2026'."""
    return datetime.now(IST).strftime("%d-%b-%Y")


def _time_display() -> str:
    """Human-readable time, e.g. '14:35'."""
    return datetime.now(IST).strftime("%H:%M")


def _gen_id() -> str:
    """Short unique ID for log entries."""
    return uuid.uuid4().hex[:12]


