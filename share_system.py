# Broadcast / Share-to-All
# ═══════════════════════════════════════════════════════════════════════════════

def share_to_all(
    page_name: str,
    data: dict = None,
    channels: list = None,
) -> list:
    """
    Send to multiple channels in one call.

    *channels* is a list of dicts, each with:
        ``{"type": "whatsapp"|"telegram"|"email", "recipient": "..."}``

    If *channels* is ``None`` or empty, returns an empty list.
    Returns a list of per-channel result dicts.
    """
    if not channels:
        return []

    results = []
    for ch in channels:
        ch_type = ch.get("type", "").lower()
        recipient = ch.get("recipient", "")

        try:
            if ch_type == "whatsapp":
                res = share_whatsapp(recipient, page_name, data)
            elif ch_type == "telegram":
                res = share_telegram(recipient, page_name, data)
            elif ch_type == "email":
                res = share_email(recipient, page_name, data)
            elif ch_type == "pdf":
                pdf_bytes = share_pdf(page_name, data)
                res = {
                    "success": pdf_bytes is not None,
                    "message_id": f"pdf-{_gen_id()}" if pdf_bytes else None,
                    "error": "" if pdf_bytes else "PDF generation failed",
                    "pdf_bytes": pdf_bytes,
                }
            else:
                res = {
                    "success": False,
                    "message_id": None,
                    "error": f"Unknown channel type: {ch_type}",
                }
        except Exception as exc:
            res = {
                "success": False,
                "message_id": None,
                "error": str(exc),
            }

        res["channel"] = ch_type
        res["recipient"] = recipient
        results.append(res)

    return results


