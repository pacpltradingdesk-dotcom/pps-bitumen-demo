def _format_data_lines(data: dict) -> list[str]:
    """
    Extract 5-8 concise data lines from a data dict.

    Supported keys in *data*:
        summary    : str       — single-line summary
        kpis       : list[dict]— each has 'label', 'value', optional 'delta'
        table_data : list      — list of dicts or lists (first few rows)
        chart_url  : str       — link to an online chart image
    """
    lines: list[str] = []
    if not data:
        return lines

    # Summary
    summary = data.get("summary")
    if summary:
        lines.append(summary)

    # KPIs
    kpis = data.get("kpis") or []
    for kpi in kpis[:6]:
        label = kpi.get("label", "")
        value = kpi.get("value", "")
        delta = kpi.get("delta", "")
        if delta:
            lines.append(f"  {label}: {value}  ({delta})")
        else:
            lines.append(f"  {label}: {value}")

    # Table (first 3 rows, compact)
    table_data = data.get("table_data") or []
    for row in table_data[:3]:
        if isinstance(row, dict):
            parts = [f"{k}: {v}" for k, v in row.items()]
            lines.append("  " + " | ".join(parts))
        elif isinstance(row, (list, tuple)):
            lines.append("  " + " | ".join(str(c) for c in row))

    # Chart link
    chart_url = data.get("chart_url")
    if chart_url:
        lines.append(f"  Chart: {chart_url}")

    return lines[:8]  # cap at 8 lines


def format_message(
    page_name: str,
    data: dict = None,
    channel: str = "whatsapp",
) -> str:
    """
    Format share message for a given channel.

    WhatsApp / Telegram (compact):
        Emoji header, date/time IST, key data points, footer.

    Email (detailed):
        HTML body with company header, tabular data, confidential footer.
    """
    date_str = _date_display()
    time_str = _time_display()
    data_lines = _format_data_lines(data)

    # ── WhatsApp / Telegram (plain-text, compact) ────────────────────────
    if channel in ("whatsapp", "telegram"):
        body_lines = data_lines if data_lines else ["No additional data."]
        msg = (
            f"\U0001f3db\ufe0f PPS ANANTAM \u2014 {page_name}\n"
            f"{date_str}, {time_str} IST\n"
            f"\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\n"
            + "\n".join(body_lines) + "\n"
            f"\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\u2501\n"
            f"Generated by PPS Bitumen Dashboard"
        )
        return msg

    # ── Email (HTML) ─────────────────────────────────────────────────────
    if channel == "email":
        # Build KPI rows for HTML table
        kpi_html = ""
        kpis = (data or {}).get("kpis") or []
        if kpis:
            kpi_rows = ""
            for kpi in kpis:
                label = kpi.get("label", "")
                value = kpi.get("value", "")
                delta = kpi.get("delta", "")
                delta_span = (
                    f' <span style="color:#4CAF50;font-size:12px;">({delta})</span>'
                    if delta else ""
                )
                kpi_rows += (
                    f"<tr>"
                    f"<td style='padding:6px 12px;border:1px solid #ddd;'>{label}</td>"
                    f"<td style='padding:6px 12px;border:1px solid #ddd;"
                    f"font-weight:bold;'>{value}{delta_span}</td>"
                    f"</tr>\n"
                )
            kpi_html = (
                "<table style='border-collapse:collapse;width:100%;margin:12px 0;'>"
                "<thead><tr>"
                "<th style='padding:8px 12px;background:#1B2A4A;color:#fff;"
                "border:1px solid #1B2A4A;text-align:left;'>Metric</th>"
                "<th style='padding:8px 12px;background:#1B2A4A;color:#fff;"
                "border:1px solid #1B2A4A;text-align:left;'>Value</th>"
                "</tr></thead><tbody>\n"
                + kpi_rows +
                "</tbody></table>"
            )

        # Build table data rows
        table_html = ""
        table_data = (data or {}).get("table_data") or []
        if table_data:
            # Determine headers
            if table_data and isinstance(table_data[0], dict):
                headers = list(table_data[0].keys())
                trows = ""
                for row in table_data[:20]:
                    cells = "".join(
                        f"<td style='padding:6px 12px;border:1px solid #ddd;'>"
                        f"{row.get(h, '')}</td>"
                        for h in headers
                    )
                    trows += f"<tr>{cells}</tr>\n"
                thead = "".join(
                    f"<th style='padding:8px 12px;background:#1B2A4A;color:#fff;"
                    f"border:1px solid #1B2A4A;text-align:left;'>{h}</th>"
                    for h in headers
                )
            elif table_data and isinstance(table_data[0], (list, tuple)):
                thead = "".join(
                    f"<th style='padding:8px 12px;background:#1B2A4A;color:#fff;"
                    f"border:1px solid #1B2A4A;text-align:left;'>Col {i+1}</th>"
                    for i in range(len(table_data[0]))
                )
                trows = ""
                for row in table_data[:20]:
                    cells = "".join(
                        f"<td style='padding:6px 12px;border:1px solid #ddd;'>"
                        f"{c}</td>"
                        for c in row
                    )
                    trows += f"<tr>{cells}</tr>\n"
            else:
                thead = ""
                trows = ""

            if thead:
                table_html = (
                    "<table style='border-collapse:collapse;width:100%;"
                    "margin:12px 0;'>"
                    f"<thead><tr>{thead}</tr></thead>"
                    f"<tbody>{trows}</tbody></table>"
                )

        summary_text = (data or {}).get("summary", "")
        summary_html = (
            f"<p style='font-size:14px;color:#333;'>{summary_text}</p>"
            if summary_text else ""
        )

        chart_url = (data or {}).get("chart_url", "")
        chart_html = (
            f"<p><img src='{chart_url}' alt='Chart' "
            f"style='max-width:100%;border:1px solid #ddd;'/></p>"
            if chart_url else ""
        )

        html = f"""\
<div style="font-family:Segoe UI,Arial,sans-serif;max-width:700px;margin:auto;">
  <!-- Header -->
  <div style="background:#1B2A4A;padding:18px 24px;border-radius:8px 8px 0 0;">
    <h1 style="margin:0;color:#D4AF37;font-size:22px;">
      \U0001f3db\ufe0f {_COMPANY_NAME}
    </h1>
    <p style="margin:4px 0 0;color:#ccc;font-size:13px;">
      {page_name} &nbsp;|&nbsp; {date_str} &nbsp;|&nbsp; {time_str} IST
    </p>
  </div>

  <!-- Body -->
  <div style="padding:20px 24px;background:#fff;border:1px solid #e0e0e0;">
    {summary_html}
    {kpi_html}
    {table_html}
    {chart_html}
  </div>

  <!-- Footer -->
  <div style="background:#f5f5f5;padding:14px 24px;border-radius:0 0 8px 8px;
              border:1px solid #e0e0e0;border-top:none;font-size:11px;color:#888;">
    <p style="margin:0 0 6px;">
      Generated by <strong>PPS Bitumen Dashboard</strong>
    </p>
    <p style="margin:0;font-style:italic;">
      {_CONFIDENTIAL_FOOTER}
    </p>
  </div>
</div>"""
        return html

    # ── Fallback: treat like WhatsApp ────────────────────────────────────
    return format_message(page_name, data, channel="whatsapp")


# ═══════════════════════════════════════════════════════════════════════════════
# Logging
# ═══════════════════════════════════════════════════════════════════════════════

def _log_share(
    channel: str,
    page_name: str,
    recipient: str,
    status: str,
    error: str = "",
) -> None:
    """
    Log a share action to share_history.json.
    Keeps the most recent *_MAX_HISTORY* entries.
    """
    entry = {
        "id": _gen_id(),
        "timestamp": _now_ist(),
        "channel": channel,
        "page_name": page_name,
        "recipient": recipient,
        "status": status,
        "error": error,
    }
    try:
        history: list = []
        if _HISTORY_FILE.exists():
            raw = _HISTORY_FILE.read_text(encoding="utf-8").strip()
            if raw:
                history = json.loads(raw)
        history.append(entry)
        # Keep only the latest _MAX_HISTORY entries
        history = history[-_MAX_HISTORY:]
        _HISTORY_FILE.write_text(
            json.dumps(history, indent=2, ensure_ascii=False),
            encoding="utf-8",
        )
    except Exception:
        pass  # logging must never crash the caller


def get_share_history(limit: int = 50) -> list:
    """Return the most recent *limit* share-history entries (newest first)."""
    try:
        if not _HISTORY_FILE.exists():
            return []
        raw = _HISTORY_FILE.read_text(encoding="utf-8").strip()
        if not raw:
            return []
        history = json.loads(raw)
        return list(reversed(history[-limit:]))
    except Exception:
        return []


